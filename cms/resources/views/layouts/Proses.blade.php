    <h5 style="color:rgb(245, 137, 14)">Cara Membuat</h5>
    </div>
    <ul class="list-group">
    <li class="list-group-item">1. Buatlah cake cokelat sesuai dengan arahan kemasan cake mix. Biarkan cake-nya mendingin terlebih dahulu, kemudian potong cake secara horizontal hingga terbagi menjadi 4 potongan tipis.</li>
    <li class="list-group-item">2. Gabungkan susu, gula halus, dan marshmallow dalam panci dan panaskan di atas api sedang. Aduk hingga gula larut dan marshmallow meleleh, kemudian angkat panci dan masukkan kelapa. Oleskan campuran ini di antara potongan cake tadi.</li>
    <li class="list-group-item">3. Panaskan heavy cream di panci hingga mendidih dan tambahkan chocolate chip. Aduk terus-menerus hingga cokelatnya meleleh. Lalu, balurkan cokelat ini di atas cake.</li>
    <li class="list-group-item">4 .Untuk mendapatkan rasa gurih kelapa, tambahkan kelapa di atasnya.</li>
    </ul>
